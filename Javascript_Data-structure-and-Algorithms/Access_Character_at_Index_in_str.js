// charAt() -> to get character at the specified index in the string
const str = "Hello, World!";
console.log(str.charAt(4));

// Because strings can be treadted like arrays, use the index via bracket notation
const string = "Hello, World!";
console.log(string[0]);

// To get the character code of the character at a specified index, use charCodeAt()
const string1 = 'Hello, World!';
console.log(string1.charCodeAt(4));